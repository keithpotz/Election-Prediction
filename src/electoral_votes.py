def get_electoral_votes(year):
    if year >= 2024:
        return EV_2024
    elif year >= 2012:
        return EV_2010
    elif year >= 2004:
        return EV_2000
    elif year >= 1992:
        return EV_1990
    elif year >= 1984:
        return EV_1980
    else:
        return EV_1970

def calculate_predicted_ec(state_results, year):
    """
    Calculate EC from model predictions.
    state_results: DataFrame with columns [state, party, predicted_pct]
    """
    ev_map = get_electoral_votes(year)
    ec_totals = {}
    state_detail = {}

    for state, sdf in state_results.groupby('state'):
        if state not in ev_map:
            continue
        ev = ev_map[state]
        winner_row = sdf.loc[sdf['predicted_pct'].idxmax()]
        party = winner_row['party']
        ec_totals[party] = ec_totals.get(party, 0) + ev

        dem_pct = sdf.loc[sdf['party'] == 'DEMOCRAT', 'predicted_pct'].values
        rep_pct = sdf.loc[sdf['party'] == 'REPUBLICAN', 'predicted_pct'].values
        state_detail[state] = {
            'winner': party,
            'ev': ev,
            'dem_pct': round(float(dem_pct[0]), 2) if len(dem_pct) else 0,
            'rep_pct': round(float(rep_pct[0]), 2) if len(rep_pct) else 0,
        }

    dem_ev = ec_totals.get('DEMOCRAT', 0)
    rep_ev = ec_totals.get('REPUBLICAN', 0)
    winner = 'DEMOCRAT' if dem_ev >= 270 else ('REPUBLICAN' if rep_ev >= 270 else 'NO MAJORITY')
    return {'DEMOCRAT': dem_ev, 'REPUBLICAN': rep_ev, 'winner': winner, 'states': state_detail}


def calculate_ec_result(df, year):
    """
    Given a dataframe of election results for a single year (all states),
    returns a dict with electoral votes per party and the EC winner.
    """
    ev_map = get_electoral_votes(year)
    ec_totals = {}

    for state, state_df in df.groupby("state"):
        if state not in ev_map:
            continue
        ev = ev_map[state]
        winner_row = state_df.loc[state_df["votes"].idxmax()]
        party = winner_row["party"]
        ec_totals[party] = ec_totals.get(party, 0) + ev

    dem_ev = ec_totals.get("DEMOCRAT", 0)
    rep_ev = ec_totals.get("REPUBLICAN", 0)
    ec_winner = "DEMOCRAT" if dem_ev > rep_ev else "REPUBLICAN"

    return {"DEMOCRAT": dem_ev, "REPUBLICAN": rep_ev, "winner": ec_winner}
